public class ModelDokumentasi {

    private String username, id_dok, nama_dok, foto_dok, point_dok, keterangan, kabupaten, kecamatan, kelurahan;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getId_dok() {
        return id_dok;
    }

    public void setId_dok(String id_dok) {
        this.id_dok = id_dok;
    }

    public String getNama_dok() {
        return nama_dok;
    }

    public void setNama_dok(String nama_dok) {
        this.nama_dok = nama_dok;
    }

    public String getFoto_dok() {
        return foto_dok;
    }

    public void setFoto_dok(String foto_dok) {
        this.foto_dok = foto_dok;
    }

    public String getPoint_dok() {
        return point_dok;
    }

    public void setPoint_dok(String point_dok) {
        this.point_dok = point_dok;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getKabupaten() {
        return kabupaten;
    }

    public void setKabupaten(String kabupaten) {
        this.kabupaten = kabupaten;
    }

    public String getKecamatan() {
        return kecamatan;
    }

    public void setKecamatan(String kecamatan) {
        this.kecamatan = kecamatan;
    }

    public String getKelurahan() {
        return kelurahan;
    }

    public void setKelurahan(String kelurahan) {
        this.kelurahan = kelurahan;
    }

}
